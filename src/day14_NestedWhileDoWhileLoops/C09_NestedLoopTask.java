package day14_NestedWhileDoWhileLoops;

public class C09_NestedLoopTask {
    public static void main(String[] args) {
        for(int i=1;i<=22;i++){
            System.out.print("Cim");
            for (int j=1; j<=2;j++){
                System.out.print("Bom");
            }
            System.out.println();
        }


    }
}


