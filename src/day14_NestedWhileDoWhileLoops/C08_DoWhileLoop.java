package day14_NestedWhileDoWhileLoops;

public class C08_DoWhileLoop {
    public static void main(String[] args) {
        boolean a = false;

        for (int i = 0;a;){
            System.out.println("Hello World From ForLoop");
        }

        System.out.println("----------------------------");
        while (a){
            System.out.println("Hello World From While Loop");
        }

        System.out.println("----------------------------");

        do{
            System.out.println("Hello World From Do-While Loop");
        }while (a);




    }
}
